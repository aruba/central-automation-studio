var searchVisible = 0;
var transparent = true;

var transparentDemo = true;
var fixedTop = false;

var navbar_initialized = false;
var mobile_menu_visible = 0,
	mobile_menu_initialized = false,
	toggle_initialized = false,
	bootstrap_nav_initialized = false,
	$sidebar,
	isWindows;

$(document).ready(function() {
	window_width = $(window).width();

	// check if there is an image set for the sidebar's background
	lbd.checkSidebarImage();

	// Init navigation toggle for small screens
	if (window_width <= 991) {
		lbd.initRightMenu();
	}

	lbd.initMinimizeSidebar();

	// Init Collapse Areas
	lbd.initCollapseArea();

	//  Activate the tooltips
	$('[rel="tooltip"]').tooltip();

	// Init Tags Input
	// if($(".tagsinput").length != 0){
	//     $(".tagsinput").tagsInput();
	// }

	//  Activate regular switches
	if ($("[data-toggle='switch']").length != 0) {
		$("[data-toggle='switch']").bootstrapSwitch();
	}

	$('.form-control')
		.on('focus', function() {
			$(this)
				.parent('.input-group')
				.addClass('input-group-focus');
		})
		.on('blur', function() {
			$(this)
				.parent('.input-group')
				.removeClass('input-group-focus');
		});

	// Fixes sub-nav not working as expected on IOS
	$('body').on('touchstart.dropdown', '.dropdown-menu', function(e) {
		e.stopPropagation();
	});
});

// activate collapse right menu when the windows is resized
$(window).resize(function() {
	if ($(window).width() <= 991) {
		lbd.initRightMenu();
	}
});

lbd = {
	misc: {
		navbar_menu_visible: 0,
	},
	checkSidebarImage: function() {
		$sidebar = $('.sidebar');
		image_src = $sidebar.data('image');

		if (image_src !== undefined) {
			sidebar_container = '<div class="sidebar-background" style="background-image: url(' + image_src + ') "/>';
			$sidebar.append(sidebar_container);
		} else if (mobile_menu_initialized == true) {
			// reset all the additions that we made for the sidebar wrapper only if the screen is bigger than 991px
			$sidebar_wrapper.find('.navbar-form').remove();
			$sidebar_wrapper.find('.nav-mobile-menu').remove();

			mobile_menu_initialized = false;
		}
	},

	initRightMenu: function() {
		$sidebar_wrapper = $('.sidebar-wrapper');

		//console.log('aici se face meniu in dreapta');

		if (!mobile_menu_initialized) {
			$navbar = $('nav')
				.find('.navbar-collapse')
				.first()
				.clone(true);

			nav_content = '';
			mobile_menu_content = '';

			//add the content from the regular header to the mobile menu
			//pas = 1;
			$navbar.children('ul').each(function() {
				content_buff = $(this).html();
				nav_content = nav_content + content_buff;
				//console.log('pas:' + pas);

				//pas = pas+1;
			});

			nav_content = '<ul class="nav nav-mobile-menu">' + nav_content + '</ul>';

			$sidebar_nav = $sidebar_wrapper.find(' > .nav');

			// insert the navbar form before the sidebar list
			$nav_content = $(nav_content);
			$nav_content.insertBefore($sidebar_nav);

			$('.sidebar-wrapper .dropdown .dropdown-menu > li > a').click(function(event) {
				event.stopPropagation();
			});

			mobile_menu_initialized = true;
		} else {
			console.log('window with:' + $(window).width());
			if ($(window).width() > 991) {
				// reset all the additions that we made for the sidebar wrapper only if the screen is bigger than 991px
				$sidebar_wrapper.find('.navbar-form').remove();
				$sidebar_wrapper.find('.nav-mobile-menu').remove();

				mobile_menu_initialized = false;
			}
		}

		if (!toggle_initialized) {
			$toggle = $('.navbar-toggler');

			$toggle.click(function() {
				if (mobile_menu_visible == 1) {
					$('html').removeClass('nav-open');

					$('.close-layer').remove();
					setTimeout(function() {
						$toggle.removeClass('toggled');
					}, 400);

					mobile_menu_visible = 0;
				} else {
					setTimeout(function() {
						$toggle.addClass('toggled');
					}, 430);

					main_panel_height = $('.main-panel')[0].scrollHeight;
					$layer = $('<div class="close-layer"></div>');
					$layer.css('height', main_panel_height + 'px');
					$layer.appendTo('.main-panel');

					setTimeout(function() {
						$layer.addClass('visible');
					}, 100);

					$layer.click(function() {
						$('html').removeClass('nav-open');
						mobile_menu_visible = 0;

						$layer.removeClass('visible');

						setTimeout(function() {
							$layer.remove();
							$toggle.removeClass('toggled');
						}, 400);
					});

					$('html').addClass('nav-open');
					mobile_menu_visible = 1;
				}
			});

			toggle_initialized = true;
		}
	},

	initMinimizeSidebar: function() {
		// when we are on a Desktop Screen and the collapse is triggered we check if the sidebar mini is active or not. If it is active then we don't let the collapse to show the elements because the elements from the collapse are showing on the hover state over the icons in sidebar mini, not on the click.
		$('.sidebar .collapse').on('in.bs.collapse', function() {
			if ($(window).width() > 991) {
				if (lbd.misc.sidebar_mini_active == true) {
					return false;
				} else {
					return true;
				}
			}
		});

		$('#minimizeSidebar').click(function() {
			var $btn = $(this);

			if (lbd.misc.sidebar_mini_active == true) {
				$('body').removeClass('sidebar-mini');
				lbd.misc.sidebar_mini_active = false;

				if (isWindows) {
					$('.sidebar .sidebar-wrapper').perfectScrollbar();
				}
			} else {
				$('.sidebar .collapse')
					.collapse('hide')
					.on('hidden.bs.collapse', function() {
						$(this).css('height', 'auto');
					});

				if (isWindows) {
					$('.sidebar .sidebar-wrapper').perfectScrollbar('destroy');
				}

				setTimeout(function() {
					$('body').addClass('sidebar-mini');

					$('.sidebar .collapse').css('height', 'auto');
					lbd.misc.sidebar_mini_active = true;
				}, 300);
			}

			// we simulate the window Resize so the charts will get updated in realtime.
			var simulateWindowResize = setInterval(function() {
				window.dispatchEvent(new Event('resize'));
			}, 180);

			// we stop the simulation of Window Resize after the animations are completed
			setTimeout(function() {
				clearInterval(simulateWindowResize);
			}, 1000);
		});
	},

	initCollapseArea: function() {
		$('[data-toggle]').each(function() {
			var thisdiv = $(this).hasClass('card-collapse');
			$(thisdiv).addClass('collapse-preview');
		});

		$('[data-toggle="collapse-hover"]')
			.hover(
				function() {
					var thisdiv = $(this).attr('data-target');
					if (!$(this).hasClass('state-open')) {
						$(this).addClass('state-hover');
						$(thisdiv).css({
							height: '30px',
							display: 'block',
							overflow: 'hidden',
						});
					}
				},
				function() {
					var thisdiv = $(this).attr('data-target');
					$(this).removeClass('state-hover');

					if (!$(this).hasClass('state-open')) {
						$(thisdiv).css({
							height: '0px',
						});
					}
				}
			)
			.click(function(event) {
				event.preventDefault();

				var thisdiv = $(this).attr('data-target');
				var height = $(thisdiv)
					.children('.card-body')
					.height();

				if ($(this).hasClass('state-open')) {
					$(thisdiv).css({
						height: '0px',
					});
					$(this).removeClass('state-open');
				} else {
					$(thisdiv).css({
						height: height + 30,
					});
					$(this).addClass('state-open');
				}
			});
	},
};

// Returns a function, that, as long as it continues to be invoked, will not
// be triggered. The function will be called after it stops being called for
// N milliseconds. If `immediate` is passed, trigger the function on the
// leading edge, instead of the trailing.

function debounce(func, wait, immediate) {
	var timeout;
	return function() {
		var context = this,
			args = arguments;
		clearTimeout(timeout);
		timeout = setTimeout(function() {
			timeout = null;
			if (!immediate) func.apply(context, args);
		}, wait);
		if (immediate && !timeout) func.apply(context, args);
	};
}
